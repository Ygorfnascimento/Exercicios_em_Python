print("1+1")  # String literal

print("Texto em aspas simples")  # String comum

print(1 + 1)  # Soma

print(1 - 1)  # Subtração

print(8 / 2)  # Divisão

print(3 * 5)  # Multiplicação

print(8 % 2)  # Resto da divisão (módulo)

print(10 / 2)  # Divisão com inteiros

print(10.0 / 2)  # Divisão com float e inteiro

print(10 / 2.0)  # Divisão com inteiro e float

print(10.0 / 2.0)  # Divisão com dois floats

print(10.0 * 2)  # Multiplicação com float e inteiro

print(10 * 2)  # Multiplicação com inteiros

print("Mariano")  # String

print("Ferraz")  # String

print("Mariano" + "Ferraz")  # Concatenação sem espaço

print("Mariano", "Ferraz")  # Concatenação com espaço automático
